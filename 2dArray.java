    public static void main(String[] args) {

        int[][] array = { { 1, 3, 5},{4, 9, 2},{9, 4, 7}};
        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[i].length; j++) {

                System.out.println(array[i][j]);
            }

        }
    }
